            <p class="header">What are the recommended settings for uploading a video?</p>
            <p>
                We prefer you upload your video in its original format and in the highest
                quality possible.
                <br /><br />
                We also like de-interlaced files. Audio and video lengths should always
                be the same with audio starting at the same time as the video.
                <br /><br />
                TapePlay supports a wide variety of video and audio formats. Here is a list
                of some well-known formats that TapePlay supports:
                <br/><br/>
                AAC, AVI, 3GP, FLV, MOV, MP3, MP4, MPEG, OGG, WAV, WEBM, WMA, and WMV.
                <br/><br/>
                Want a quick overview of what may be an issue with your video's formatting?
                Pay attention to any warnings or error messages while you're uploading a video.
            </p>
            <p class="header">How do I upload a video?</p>
            <p>
                Remember, you have to be an athlete aiming to play at the next level in
                order to upload a video. The only other people who can upload a video are
                the featured athlete's parents and legal guardians.
                <br /><br />
                To upload a video, you've got to have a Player account.
                <br/><br />
                Once you have a Player account, go to the upload page. This is where you can upload
                your video.
                <br /><br />
                Then hit the Browse button, select the file you want to upload from your computer,
                and hit the Select button. Your video will start uploading and you will see a progress
                bar telling you how long it will take.
                <br/><br/>
                (Please note: slower connections and large files will take longer so please be patient.)
                <br /><br />
                After your video is finished uploading, it will go through a conversion process. Once
                uploaded, the video will appear both on the site and in your account.
                <br/><br />
                It's that simple.
            </p>
